#include<bits/stdc++.h>
using namespace std;

const long long N=1e6;
int d(){
    int ret;
    scanf("%d",&ret);
    return ret;
}
long long lld(){
    long long ret;
    scanf("%lld",&ret);
    return ret;
}
vector<string>v;


int main(){
    int n=d(),m=d();
    int ans=0;
    for(int i=0;i<n;i++){
        string s;
        cin>>s;
        v.push_back(s);
    }

     for(int i=0;i<n;i++){
        for(int j=0;j<m;j++){
            int temp=v[i][j]-48,mx=-1;
            for(int a=0;a<n;a++){
                mx=max(mx,v[a][j]-48);
            }
            if(mx==temp){
            ans++;
            break;
            }
        }
    }


    printf("%d",ans);

}


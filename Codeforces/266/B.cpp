#include<bits/stdc++.h>
using namespace std;

const long long N=1e6;
int d(){
    int ret;
    scanf("%d",&ret);
    return ret;
}
long long lld(){
    long long ret;
    scanf("%lld",&ret);
    return ret;
}
char arr[N];
int main(){
    int n=d(),t=d();
    scanf("%s",arr);
    for(int j=0;j<t;j++){
        for(int i=0;i<n;i++){
            if(arr[i]=='B'&&arr[i+1]=='G'){
                arr[i]='G';
                arr[i+1]='B';
                i++;

            }
        }
    }
    printf("%s",arr);
}

